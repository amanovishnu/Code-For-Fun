# Create a simple calculator for addition, substraction, multiplication and division using functions
def addition(a,b):
    return a+b
def subtraction(a,b):
    return a-b
def multiply(a,b):
    return a*b
def division(a,b):
    return a/b
def modulus(a,b):
    return a%b
result = addition(5,6)
print("Addition:",result)
result = subtraction(5,6)
print("Subtraction:",result)
result = multiply(5,6)
print("Multiply:",result)
result = division(5,6)
print("Division:",result)
result = modulus(4,2)
print("Modulus:",result)
